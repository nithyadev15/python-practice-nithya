# It is used to compare two values
def test_Comparison_operators():
    # Equal
    number = 5
    assert number == 5

    # Not Equal
    number = 5
    assert number != 3

    # Greater than
    number = 5
    assert number > 3

    # Lower than
    number = 5
    assert number < 8

    # Greater than or equal to
    number = 5
    assert number >= 5
    assert number >= 4

    # Lesser than or equal to
    number = 5
    assert number <= 4
    assert number <= 5

